"""
입력 정답 출력
321   9  10
609  10  11
642  10  11
643  11  12
645  11  12
963  10  11
"""

n = int(input())
x = [0] * (10 ** 6 + 1)
x[1] = 0
x[2] = 1
x[3] = 1
for i in range (2, n+1):
    x[i] = x[i-1] + 1
    if i % 3 == 0:
        x[i] = min(x[i//3]+1, x[i-1]+1, x[i])
    if i % 2 == 0:
        x[i] = min(x[i//2]+1, x[i-1]+1, x[i])
print(x[n])
